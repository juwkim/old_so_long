#include "game.h"

void	monster_position(t_monster *m)
{
	m->a[0] = m->pp[0];
	m->a[1] = m->pp[1];
	m->b[0] = m->pp[0];
	m->b[1] = m->pp[1] + BPX;
	m->c[0] = m->pp[0] + BPX;
	m->c[1] = m->pp[1];
	m->d[0] = m->pp[0] + BPX;
	m->d[1] = m->pp[1] + BPX;
}
